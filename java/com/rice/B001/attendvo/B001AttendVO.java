package com.rice.B001.attendvo;

public class B001AttendVO {
	
	private String attendDate;
	private String attendInfo;
	private String attendingTime;
	
	
	public String getAttendDate() {
		return attendDate;
	}
	public void setAttendDate(String attendDate) {
		this.attendDate = attendDate;
	}
	public String getAttendInfo() {
		return attendInfo;
	}
	public void setAttendInfo(String attendInfo) {
		this.attendInfo = attendInfo;
	}
	public String getAttendingTime() {
		return attendingTime;
	}
	public void setAttendingTime(String attendingTime) {
		this.attendingTime = attendingTime;
	}
}
